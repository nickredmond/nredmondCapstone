package app;

public class ComplexNumberMath {
	public static ComplexNumber multiplyByScalar(ComplexNumber number, float scalar){
		return new ComplexNumber((number.a() * scalar), (number.b() * scalar));
	}
	
	public static ComplexNumber add(ComplexNumber number1, ComplexNumber number2){
		return new ComplexNumber((number1.a() + number2.a()), (number1.b() + number2.b()));
	}
}
